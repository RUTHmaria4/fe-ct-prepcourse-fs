function devolverUltimoElemento(array) {
  // Retornar el último elemento del arreglo recibido.
  // Tu código:
  return array[array.length - 1]
}

module.exports = devolverUltimoElemento;


//EJEMPLO
 console.log(devolverUltimoElemento([10, 20, 30, 40, 17]));
 console.log(devolverUltimoElemento(["aprender", "atender", "escuchar", "sentir", "elegir"]));

